//! Marching cubes mesh extraction from SDF.

use crate::Stock;

/// Marching cubes mesh extraction.
pub struct MarchingCubes;

impl MarchingCubes {
    /// Extract a triangle mesh from a stock using marching cubes.
    ///
    /// # Returns
    ///
    /// A tuple of (vertices, indices) where vertices are [x, y, z] positions
    /// and indices are triangle vertex indices.
    pub fn extract(stock: &Stock) -> (Vec<[f64; 3]>, Vec<u32>) {
        let bounds = stock.bounds();
        let resolution = stock.resolution();

        let nx = ((bounds[3] - bounds[0]) / resolution).ceil() as usize + 1;
        let ny = ((bounds[4] - bounds[1]) / resolution).ceil() as usize + 1;
        let nz = ((bounds[5] - bounds[2]) / resolution).ceil() as usize + 1;

        let mut vertices = Vec::new();
        let mut indices = Vec::new();

        // Sample SDF values at grid points
        let mut sdf_grid = vec![0.0_f64; nx * ny * nz];
        for iz in 0..nz {
            for iy in 0..ny {
                for ix in 0..nx {
                    let x = bounds[0] + ix as f64 * resolution;
                    let y = bounds[1] + iy as f64 * resolution;
                    let z = bounds[2] + iz as f64 * resolution;
                    sdf_grid[iz * ny * nx + iy * nx + ix] = stock.sdf_at(x, y, z);
                }
            }
        }

        // Process each cell
        for iz in 0..nz - 1 {
            for iy in 0..ny - 1 {
                for ix in 0..nx - 1 {
                    let cell_vertices = [
                        (ix, iy, iz),
                        (ix + 1, iy, iz),
                        (ix + 1, iy + 1, iz),
                        (ix, iy + 1, iz),
                        (ix, iy, iz + 1),
                        (ix + 1, iy, iz + 1),
                        (ix + 1, iy + 1, iz + 1),
                        (ix, iy + 1, iz + 1),
                    ];

                    let cell_sdf: [f64; 8] = std::array::from_fn(|i| {
                        let (cx, cy, cz) = cell_vertices[i];
                        sdf_grid[cz * ny * nx + cy * nx + cx]
                    });

                    // Compute cube index
                    let mut cube_index = 0;
                    for (i, &sdf) in cell_sdf.iter().enumerate() {
                        if sdf < 0.0 {
                            cube_index |= 1 << i;
                        }
                    }

                    // Skip if entirely inside or outside
                    if cube_index == 0 || cube_index == 255 {
                        continue;
                    }

                    // Get triangles for this configuration
                    let edge_flags = EDGE_TABLE[cube_index];
                    if edge_flags == 0 {
                        continue;
                    }

                    // Compute edge intersections
                    let mut edge_verts = [[0.0_f64; 3]; 12];
                    for edge in 0..12 {
                        if edge_flags & (1 << edge) != 0 {
                            let (v0, v1) = EDGE_VERTICES[edge];
                            let sdf0 = cell_sdf[v0];
                            let sdf1 = cell_sdf[v1];

                            let t = if (sdf1 - sdf0).abs() < 1e-10 {
                                0.5
                            } else {
                                -sdf0 / (sdf1 - sdf0)
                            };

                            let (cx0, cy0, cz0) = cell_vertices[v0];
                            let (cx1, cy1, cz1) = cell_vertices[v1];

                            let x0 = bounds[0] + cx0 as f64 * resolution;
                            let y0 = bounds[1] + cy0 as f64 * resolution;
                            let z0 = bounds[2] + cz0 as f64 * resolution;
                            let x1 = bounds[0] + cx1 as f64 * resolution;
                            let y1 = bounds[1] + cy1 as f64 * resolution;
                            let z1 = bounds[2] + cz1 as f64 * resolution;

                            edge_verts[edge] = [
                                x0 + t * (x1 - x0),
                                y0 + t * (y1 - y0),
                                z0 + t * (z1 - z0),
                            ];
                        }
                    }

                    // Add triangles
                    let tri_table = &TRI_TABLE[cube_index];
                    for chunk in tri_table.chunks(3) {
                        if chunk[0] == -1 {
                            break;
                        }
                        let base = vertices.len() as u32;
                        vertices.push(edge_verts[chunk[0] as usize]);
                        vertices.push(edge_verts[chunk[1] as usize]);
                        vertices.push(edge_verts[chunk[2] as usize]);
                        indices.push(base);
                        indices.push(base + 1);
                        indices.push(base + 2);
                    }
                }
            }
        }

        (vertices, indices)
    }
}

